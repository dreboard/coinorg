<p>Dollar coins have been minted in the United States in gold, silver, and base metal versions. The term silver dollar is often used for any large white metal coin issued by the United States with a face value of one dollar, although purists insist that a dollar is not silver unless it contains some of that metal. Silver dollars, the first dollar coin issue, were minted beginning in 1794. Gold and gold-colored dollars have also been produced by the United States. The Sacagawea and Presidential dollars are usually referred to as golden dollars, despite not containing any gold, as they are of a golden color.<br />
