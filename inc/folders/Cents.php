<p>The United States one-cent coin, commonly known as a penny, is a unit of currency equaling one one-hundredth of a United States dollar. The cent's symbol is ¢. Its obverse has featured the profile of President Abraham Lincoln since 1909, the centennial of his birth. From 1959 (the sesquicentennial of Lincoln's birth) to 2008, the reverse featured the Lincoln Memorial. Four different reverse designs in 2009 honored Lincoln's 200th birthday and a new, permanent reverse - the Union Shield - was introduced in 2010. The coin is 0.75 inches (19.05 mm) in diameter and 0.061 inches (1.55 mm) in thickness.<br />
