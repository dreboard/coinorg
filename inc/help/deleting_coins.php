<h1>1. Deleting Coins (Individual)</h1>
<p>On the main toolbar when viewing coin details click the <strong>Edit This Coin</strong> link</p>
<img src="../../img/helper/delete-coin.jpg" width="700" height="148" />
<br />
<p>On the edit page, press the <strong>Delete Coin From Collection </strong> button and confirm you want to remove the coin</p>
<p><img src="../../img/helper/delete-coin-button.jpg" width="700" height="148" /></p><p>&nbsp;</p>
<hr><a name="option1"></a>
<h1>2. Deleting Coins (Bulk) Option 1</h1>
<p>To delete multiple coins, first go to the main page of any main coin category: Small Cents, Quarters...</p>
<p>Then click the <strong>Manage</strong> Button</p>
<p><img src="../../img/helper/bulk-delete-coin.jpg" width="700" height="148" /></p>
<p>You can then click the <strong>REMOVE ALL</strong> button or delete each coin in that category on the right side of the list by clicking the Delete Coin button</p>
<p><img src="../../img/helper/bulk-all-delete-coin.jpg" width="752" height="499" /></p>
<a name="option2"></a><p>&nbsp;</p><hr>
<h1>3. Deleting Coins (Bulk) Option 2</h1>
<p>To delete multiple coins, first go to the main page of any main coin category: Small Cents, Quarters...</p>
<p>Then click the <strong>Bulk Remove</strong> Link</p>
<p><img src="../../img/helper/bulk-remove-link.jpg" width="752" height="196" /></p>
<p>Here you can select multiple coins to delete all at one by choosing them individually.</p>
<img src="../../img/helper/bulk-all-delete-coin2.jpg" width="752" height="499" />
