<p>The (United States) nickel is a five-cent coin, representing a unit of currency equaling a twentieth of one United States dollar. A later-produced Canadian nickel five-cent coin was also called by the same name.
The nickel's design since 1938 has featured a portrait of Thomas Jefferson on the obverse. From 1938 to 2003, Monticello was featured on the reverse. For 2004 and 2005, nickels featured new designs to commemorate the bicentennials of the Louisiana Purchase and the Lewis and Clark Expedition; these new designs were called the Westward Journey nickel series. In 2006, Monticello returned to the reverse, while a new image of Jefferson facing forward was featured on the obverse<br />
