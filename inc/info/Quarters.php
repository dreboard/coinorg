<p>A quarter dollar, commonly shortened to quarter, is a coin worth ¼ of a United States dollar or 25 cent . The quarter has been produced since 1796. The choice of 25¢ as a denomination, as opposed to 20¢ which is more common in other parts of the world, originated with the practice of dividing Spanish Milled Dollars into eight wedge shaped segments; at one time "two bits", i.e. two reales, was a common nickname for a quarter<br />
