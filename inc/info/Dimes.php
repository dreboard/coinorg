<p>The dime is a ten cent coin, one tenth of a United States dollar, labeled formally as "one dime". The denomination was first authorized by the Coinage Act of 1792. The dime is the smallest in diameter and is the thinnest of all U.S. coins currently minted for circulation.<br />
